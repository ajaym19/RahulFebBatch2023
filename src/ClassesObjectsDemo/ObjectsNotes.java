package ClassesObjectsDemo;

public class ObjectsNotes {

	/*
	 * Object:
	 * anything we see is an object
	 * Object has 
	 * 1. Properties:
	 * 		an attribute which defines the object
	 * 		defined as variables
	 * 
	 * 2. Behavior
	 * 		the features or actions which object can perform
	 * 		defined as methods
	 * 
	 * pen: is an object
	 * Properties: model, type, company, logo, length, weight, grip, 
	 * Behavior: write, filling, scribble, 
	 * 
	 * car:
	 * Properties: color, company, model, type, fuleType, power, seating , 
	 * Behavior: driver, reverse, brakes, accelerate, 
	 * 
	 * Human Being:
	 * Properties: gender, name, age, location, card, pan, 
	 * Behavior: thinking, walking, reading, dancing, sleeping, eating 
	 * 
	 * Class:
	 * blueprint of an object
	 * collection of similar objects
	 * 
	 * Teachers Class
	 * Students Class
	 * 
	 */
}
