package ClassesObjectsDemo;

public class VariablesMethodsTypes {

	/*
	 * Variables
	 * 1. Global: declared inside a class but outside any method
	 * 		life is throughout the class
	 * 		are initialized by default
	 * 
	 * 		a. Instance: whose values are dependent on the object
	 * 
	 * 		b. Static: whose values are not dependent on the object
	 * 
	 * 2. Local: declared inside a class and inside a method
	 * 		life is only within the method
	 * 		we need to initialize before we use it
	 * 
	 * Methods:
	 * 1. Instance:
	 * 	if the method is accessing only instance variables
	 * 	you can access both static and instance
	 * 2. Static
	 * 	if the method is accessing only static variables or no variables
	 * 	you can access only static variables
	 */
}
